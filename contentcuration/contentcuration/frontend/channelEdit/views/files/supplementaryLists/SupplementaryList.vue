<template>

  <VList two-line>
    <SupplementaryItem
      v-for="file in files"
      :key="file.id"
      :file="file"
      :presetID="presetID"
      :readonly="readonly"
      :uploadCompleteHandler="newFile => replace(file, newFile)"
      @remove="deleteFile(file)"
    />
    <Uploader
      v-if="!readonly"
      :readonly="!addingFile || !selectedLanguage"
      :presetID="presetID"
      :uploadingHandler="add"
      @upload="$emit('upload')"
    >
      <template #default="{ openFileDialog }">
        <VListTile @click.stop>
          <VListTileContent v-if="!addingFile">
            <ActionLink
              data-test="add-file"
              :text="addText"
              @click="addingFile = true"
            />
          </VListTileContent>
          <VListTileContent v-else style="max-width: 250px; height: auto;">
            <LanguageDropdown
              v-model="selectedLanguage"
              data-test="select-language"
              :excludeLanguages="currentLanguages"
              hide-details
            />
          </VListTileContent>
          <VListTileContent v-if="selectedLanguage">
            <VListTileTitle>
              <ActionLink
                data-test="upload-file"
                style="margin-left: 16px;"
                :text="$tr('selectFileText')"
                @click="openFileDialog"
              />
            </VListTileTitle>
          </VListTileContent>
          <VSpacer />
          <VListTileAction v-if="addingFile">
            <VBtn icon @click="reset">
              <Icon>
                clear
              </Icon>
            </VBtn>
          </VListTileAction>
        </VListTile>
      </template>
    </Uploader>
  </VList>

</template>

<script>

  import sortBy from 'lodash/sortBy';
  import uniqBy from 'lodash/uniqBy';
  import SupplementaryItem from './SupplementaryItem';
  import LanguageDropdown from 'shared/views/LanguageDropdown';
  import Uploader from 'shared/views/files/Uploader';
  import { File } from 'shared/data/resources';
  import useFiles from 'shared/composables/useFiles';

  export default {
    name: 'SupplementaryList',
    components: {
      SupplementaryItem,
      LanguageDropdown,
      Uploader,
    },
    props: {
      nodeFiles: {
        type: Array,
        required: true,
      },
      presetID: {
        type: String,
        required: true,
      },
      addText: {
        type: String,
        default: '',
      },
      readonly: {
        type: Boolean,
        default: false,
      },
      nodeId: {
        type: String,
        required: true,
      },
    },
    setup() {
      const { updateFile } = useFiles();
      return { updateFile };
    },
    data() {
      return {
        addingFile: false,
        selectedLanguage: null,
      };
    },
    computed: {
      files() {
        if (!this.nodeFiles) {
          return [];
        }
        return uniqBy(
          sortBy(
            this.nodeFiles.filter(f => f.preset.id === this.presetID),
            f => f.language.native_name
          ),
          f => f.language.id
        );
      },
      currentLanguages() {
        return this.files.map(f => f.language.id);
      },
    },
    methods: {
      add(file) {
        this.makeFile(file).then(this.reset);
      },
      deleteFile(file) {
        if (!file) {
          return;
        }
        File.delete(file.id);
      },
      makeFile(file) {
        return this.updateFile({
          ...file,
          language: file.language || this.selectedLanguage,
          preset: this.presetID,
          contentnode: this.nodeId,
        });
      },
      replace(oldFile, newFile) {
        newFile.language = oldFile.language;
        return Promise.all([this.makeFile(newFile), this.deleteFile(oldFile)]);
      },
      reset() {
        this.addingFile = false;
        this.selectedLanguage = null;
      },
    },
    $trs: {
      selectFileText: 'Select file',
    },
  };

</script>
